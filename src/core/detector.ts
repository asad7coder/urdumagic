const ARABIC_RE = /[\u0600-\u06FF]/;
const LATIN_RE = /[A-Za-z]/;

function stripWord(w: string): string {
  return w.replace(/^[^A-Za-z]+|[^A-Za-z]+$/g, '').toLowerCase();
}

function latinWordMatches(text: string): number {
  const words = text.split(/\s+/);
  let n = 0;
  for (const w of words) {
    const s = stripWord(w);
    if (s.length > 0 && ROMAN_URDU_STOPWORDS.has(s)) n += 1;
  }
  return n;
}

/**
 * Detect the script type of input text.
 *
 * @param text - Any text
 * @returns Script classification
 */
export function detectScript(text: string): ScriptType {
  if (text.length === 0) return 'latin';

  const hasArabic = ARABIC_RE.test(text);
  const hasLatin = LATIN_RE.test(text);

  if (hasArabic && hasLatin) return 'mixed';
  if (hasArabic && !hasLatin) return 'arabic';
  if (hasLatin && !hasArabic) {
    const matches = latinWordMatches(text);
    if (matches >= 2) return 'roman-urdu';
    if (matches === 0) return 'english';
    return 'english';
  }
  return 'latin';
}

/**
 * Returns true when the text looks like Roman Urdu (Latin letters + stopword signal).
 *
 * @param text - Input text
 */
export function isRomanUrdu(text: string): boolean {
  if (!LATIN_RE.test(text) || ARABIC_RE.test(text)) return false;
  return latinWordMatches(text) >= 2;
}

/**
 * Returns true when the text contains Urdu (Arabic) script letters.
 *
 * @param text - Input text
 */
export function isUrduScript(text: string): boolean {
  return ARABIC_RE.test(text);
}

/**
 * Returns true when any token matches the Roman Urdu stopword list.
 *
 * @param text - Input text
 */
export function hasUrduWords(text: string): boolean {
  const words = text.split(/\s+/);
  for (const w of words) {
    const s = stripWord(w);
    if (s.length > 0 && ROMAN_URDU_STOPWORDS.has(s)) return true;
  }
  return false;
}
